        <div id="description" class="fade-in">
            The standard or quality of an institution or program is greatly measured by the qualifications of its
            faculty. In this light, the faculty should be composed of competent members in terms of academic
            qualifications, experience and professional expertise. In addition, they should manifest desirable personal
            qualities and high level of professionalism.
            <br><br>
            To be effective, faculty members should be properly compensated and taken care of. They must be given
            opportunities for continuous personal and professional development. A policy of fair and equitable
            distribution of teaching assignments and workload should be practiced. Likewise, objective and clear
            promotion criteria/ scheme should be adopted by the institution.
