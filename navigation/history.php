                        Bayan sa Dambana ng Kagitingan</strong>.
                </p>

                <p>
                    Today, PUP San Juan City Campus which started as a locally funded campus is now a viable institution
                    by virtue of <strong>Republic Act 11348 on 25 July 2019</strong> and continues to grow and develop
