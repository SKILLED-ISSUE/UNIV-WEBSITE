                        Series of 2008</strong> otherwise known as “An Ordinance Establishing a PUP Campus in the City
                    of San Juan, Metro Manila”.
                </p>

                <p>
                    The signing of the MOA by Mayor Ejercito, on behalf of the City of San Juan and former University
                    President <strong>Dr. Dante G. Guevarra</strong>, on behalf of PUP on February 20, 2008 became a
                    landmark in San Juan’s long history of struggle for quality but accessible tertiary education.
                </p>

                <p>
                    Subsequently, the <strong>PUP Board of Regents</strong> approved and ratified the MOA on March 14,
                    2008 through <strong>Board Resolution No. 613, Series of 2008</strong>. Since then and for the years
