        <div id="description" class="fade-in">
            The library is the heart of any learning institution. It is a synergy of people, hardware and software whose
            purpose is to assist clients in using knowledge and technology to transform and improve their lives.
            <br><br>
            Information and knowledge are essential to the attainment of institutional goals. The ways in which they are
            selected, acquired, stored, accessed and distributed within the Institution will, in large measure,
            determine the success of teaching, research and other academic endeavors. The Institution thrives on clear
            policies concerning access to, and provision of, information. Thus, the library must take an active role in
            the development and implementation of these policies.
            <br><br>
            Each institution has a unique vision, mission, goals and objectives. These are influenced by its philosophy,
            geographical location and social responsibility. Similarly, as a subsystem of the Institution, the library
            has a unique role to perform.
